# Lloyd-Max optimal centroids for N(0, 1)
# Precomputed via the iterative Lloyd-Max algorithm for the standard normal.
# For TurboQuant, these are scaled by 1/sqrt(dim) at runtime.
# ---------------------------------------------------------------------------

_LLOYD_MAX_CENTROIDS: dict[int, NDArray[np.float64]] = {
    2: np.array([-1.5104176087, -0.4527800398, 0.4527800398, 1.5104176087]),
    3: np.array([
        -2.1519775209, -1.3439092613, -0.7560052489, -0.2451209526,
        0.2451209526, 0.7560052489, 1.3439092613, 2.1519775209,
    ]),
    4: np.array([
        -2.7326368678, -2.0690790941, -1.6180334280, -1.2562297480,
        -0.9423600572, -0.6568055658, -0.3880823641, -0.1284362997,
        0.1284362997, 0.3880823641, 0.6568055658, 0.9423600572,
        1.2562297480, 1.6180334280, 2.0690790941, 2.7326368678,
    ]),
}

# ---------------------------------------------------------------------------
# Rotation matrix helpers
# ---------------------------------------------------------------------------


@functools.lru_cache(maxsize=8)
def make_rotation_matrix(dim: int, seed: int) -> NDArray[np.float64]:
    """Generate a deterministic random orthogonal matrix via QR decomposition.

    Results are cached by (dim, seed) to avoid recomputing on every store open.
    """
    rng = np.random.default_rng(seed)
    z = rng.standard_normal((dim, dim))
    q, r = np.linalg.qr(z)
    # Ensure uniform distribution (Haar measure) by fixing sign ambiguity
    d = np.sign(np.diag(r))
    d[d == 0] = 1
    q = q * d[np.newaxis, :]
    q.flags.writeable = False  # immutable since it's cached
    return q

