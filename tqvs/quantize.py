def _pack_turbo4(indices: np.ndarray) -> np.ndarray:
    """Pack 4-bit unsigned indices (0..15) into uint8.  Two values per byte."""
    n, dim = indices.shape
    if dim % 2:
        indices = np.concatenate(
            [indices, np.zeros((n, 1), dtype=indices.dtype)], axis=1
        )
    low = indices[:, 0::2].astype(np.uint8)
    high = indices[:, 1::2].astype(np.uint8)
    return low | (high << 4)


def _unpack_turbo4(packed: np.ndarray, dim: int) -> np.ndarray:
    """Unpack uint8 → 4-bit unsigned indices (0..15)."""
    low = packed & 0x0F
    high = (packed >> 4) & 0x0F
    n = packed.shape[0]
    interleaved = np.empty((n, packed.shape[1] * 2), dtype=np.uint8)
    interleaved[:, 0::2] = low
    interleaved[:, 1::2] = high
    return interleaved[:, :dim]
