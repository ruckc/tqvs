# ---------------------------------------------------------------------------
# TurboQuant — random rotation + Lloyd-Max scalar quantization
# ---------------------------------------------------------------------------


def _get_turbo_codebook(bits: int, dim: int) -> NDArray[np.float64]:
    """Return sorted Lloyd-Max centroids scaled for dimension *dim*.

    The centroids are optimal for N(0, 1/dim) — the distribution of each
    coordinate after random rotation of a unit-norm vector in R^dim.
    """
    base = _LLOYD_MAX_CENTROIDS[bits]
    return base / math.sqrt(dim)


def _quantize_turbo(
    vectors: NDArray[np.float32],
    bits: int,
    rotation_matrix: NDArray[np.float64] | None,
) -> tuple[np.ndarray, np.ndarray]:
    """TurboQuant quantization: rotate → scalar-quantize each coordinate.

    Returns ``(packed_indices, norms)`` where *norms* has shape ``(n, 1)``.
    """
    if rotation_matrix is None:
        raise ValueError("rotation_matrix is required for TurboQuant dtypes")
    n, dim = vectors.shape
    # Normalise to unit norm; store norms as quant_params
    norms = np.linalg.norm(vectors, axis=1, keepdims=True).astype(np.float32)
    safe_norms = np.where(norms == 0, 1.0, norms)
    normalised = vectors / safe_norms  # (n, dim)

    # Rotate
    rot = rotation_matrix.astype(np.float32)
    rotated = normalised @ rot.T  # (n, dim)

    # Lloyd-Max scalar quantization per coordinate
    codebook = _get_turbo_codebook(bits, dim).astype(np.float32)  # (2^bits,)
    num_levels = len(codebook)
    # Boundaries are midpoints between consecutive centroids
    boundaries = (codebook[:-1] + codebook[1:]) / 2  # (2^bits - 1,)
    # np.searchsorted gives the index of the first boundary > value,
    # which is exactly the cluster index when centroids are sorted.
    indices = np.searchsorted(boundaries, rotated).astype(np.uint8)  # (n, dim)

    # Pack indices using the appropriate sub-byte packer
    if bits == 2:
        packed = _pack_turbo2(indices)
    elif bits == 3:
        packed = _pack_turbo3(indices)
    else:  # bits == 4
        packed = _pack_turbo4(indices)

    return packed, norms


def _dequantize_turbo(
    data: np.ndarray,
    bits: int,
    quant_params: np.ndarray | None,
    dim: int,
    rotation_matrix: NDArray[np.float64] | None,
) -> NDArray[np.float32]:
    """TurboQuant dequantization: unpack → lookup centroids → inverse-rotate → rescale."""
    assert quant_params is not None
    if rotation_matrix is None:
        raise ValueError("rotation_matrix is required for TurboQuant dtypes")

    norms = quant_params  # (n, 1)

    # Unpack indices
    if bits == 2:
        indices = _unpack_turbo2(data, dim)
    elif bits == 3:
        indices = _unpack_turbo3(data, dim)
    else:  # bits == 4
        indices = _unpack_turbo4(data, dim)

    # Reconstruct from codebook
    codebook = _get_turbo_codebook(bits, dim).astype(np.float32)
    reconstructed = codebook[indices]  # (n, dim)

    # Inverse rotation: x_hat = reconstructed @ Π  (since Π is orthogonal, Π^{-1} = Π^T)
    rot = rotation_matrix.astype(np.float32)
    unrotated = reconstructed @ rot  # (n, dim)

    return unrotated * norms


# ---------------------------------------------------------------------------
# TurboQuant 2-bit packing (4 values per byte)
# ---------------------------------------------------------------------------


def _pack_turbo2(indices: np.ndarray) -> np.ndarray:
    """Pack 2-bit unsigned indices (0..3) into uint8.

    Four values per byte.  *indices* shape ``(n, dim)`` → ``(n, ceil(dim/4))``.
    """
    n, dim = indices.shape
    pad = (4 - dim % 4) % 4
    if pad:
        indices = np.concatenate(
            [indices, np.zeros((n, pad), dtype=indices.dtype)], axis=1
        )
    reshaped = indices.reshape(n, -1, 4)  # (n, groups, 4)
    packed = (
        reshaped[:, :, 0].astype(np.uint8)
        | (reshaped[:, :, 1].astype(np.uint8) << 2)
        | (reshaped[:, :, 2].astype(np.uint8) << 4)
        | (reshaped[:, :, 3].astype(np.uint8) << 6)
    )
    return packed


def _unpack_turbo2(packed: np.ndarray, dim: int) -> np.ndarray:
    """Unpack uint8 → 2-bit unsigned indices (0..3).

    Returns shape ``(n, dim)`` with dtype uint8.
    """
    v0 = packed & 0x03
    v1 = (packed >> 2) & 0x03
    v2 = (packed >> 4) & 0x03
    v3 = (packed >> 6) & 0x03
    n = packed.shape[0]
    interleaved = np.empty((n, packed.shape[1] * 4), dtype=np.uint8)
    interleaved[:, 0::4] = v0
    interleaved[:, 1::4] = v1
    interleaved[:, 2::4] = v2
    interleaved[:, 3::4] = v3
    return interleaved[:, :dim]


# ---------------------------------------------------------------------------
# TurboQuant 3-bit packing (8 values → 3 bytes, unsigned 0..7)
# ---------------------------------------------------------------------------


def _pack_turbo3(indices: np.ndarray) -> np.ndarray:
    """Pack 3-bit unsigned indices (0..7) into uint8.

    8 values per 3 bytes (24 bits).
    *indices* shape ``(n, dim)`` → ``(n, ceil(dim/8)*3)``.
    """
    n, dim = indices.shape
    pad = (8 - dim % 8) % 8
    if pad:
        indices = np.concatenate(
            [indices, np.zeros((n, pad), dtype=indices.dtype)], axis=1
        )
    groups = indices.shape[1] // 8
    vals = indices.astype(np.uint32).reshape(n, groups, 8)

    bits = np.zeros((n, groups), dtype=np.uint32)
    for i in range(8):
        bits |= vals[:, :, i] << (i * 3)

    packed = np.zeros((n, groups * 3), dtype=np.uint8)
    packed[:, 0::3] = (bits & 0xFF).astype(np.uint8)
    packed[:, 1::3] = ((bits >> 8) & 0xFF).astype(np.uint8)
    packed[:, 2::3] = ((bits >> 16) & 0xFF).astype(np.uint8)
    return packed


def _unpack_turbo3(packed: np.ndarray, dim: int) -> np.ndarray:
    """Unpack uint8 → 3-bit unsigned indices (0..7).

    Returns shape ``(n, dim)`` with dtype uint8.
    """
    n = packed.shape[0]
    groups = packed.shape[1] // 3

    bits = (
        packed[:, 0::3].astype(np.uint32)
        | (packed[:, 1::3].astype(np.uint32) << 8)
        | (packed[:, 2::3].astype(np.uint32) << 16)
    )

    vals = np.zeros((n, groups, 8), dtype=np.uint8)
    for i in range(8):
        vals[:, :, i] = ((bits >> (i * 3)) & 0x07).astype(np.uint8)

    return vals.reshape(n, groups * 8)[:, :dim]


# ---------------------------------------------------------------------------
# TurboQuant 4-bit packing (2 values per byte, unsigned 0..15)
