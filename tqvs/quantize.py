def quantize(
    vectors: NDArray[np.floating],
    dtype: StoreDtype,
    *,
    rotation_matrix: NDArray[np.float64] | None = None,
) -> tuple[np.ndarray, np.ndarray | None]:
    """Quantize *vectors* (float32/64, shape ``(n, dim)``) to *dtype*.

    Returns
    -------
    data : np.ndarray
        Stored representation.  Shape depends on dtype.
    quant_params : np.ndarray | None
        Per-vector parameters (scale and optionally zero-point).
        ``None`` for float dtypes.

    Parameters
    ----------
    rotation_matrix : optional
        Required for TurboQuant dtypes.  A ``(dim, dim)`` orthogonal matrix
        generated by :func:`make_rotation_matrix`.
    """
    vectors = np.asarray(vectors, dtype=np.float32)
    if vectors.ndim == 1:
        vectors = vectors[np.newaxis, :]

    match dtype:
        case StoreDtype.FLOAT64:
            return vectors.astype(np.float64), None
        case StoreDtype.FLOAT32:
            return vectors.astype(np.float32), None
        case StoreDtype.FLOAT16:
            return vectors.astype(np.float16), None
        case StoreDtype.BFLOAT16:
            return _float32_to_bfloat16(vectors), None
        case StoreDtype.INT8_SYM:
            return _quantize_int8_sym(vectors)
        case StoreDtype.INT8_ASYM:
            return _quantize_int8_asym(vectors)
        case StoreDtype.FP4:
            return _quantize_fp4(vectors)
        case StoreDtype.INT4:
            return _quantize_int4(vectors)
        case StoreDtype.INT3:
            return _quantize_int3(vectors)
        case StoreDtype.TURBO_2BIT:
            return _quantize_turbo(vectors, 2, rotation_matrix)
        case StoreDtype.TURBO_3BIT:
            return _quantize_turbo(vectors, 3, rotation_matrix)
        case StoreDtype.TURBO_4BIT:
            return _quantize_turbo(vectors, 4, rotation_matrix)
        case _:  # pragma: no cover
            raise ValueError(f"Unsupported dtype: {dtype}")


def dequantize(
    data: np.ndarray,
    dtype: StoreDtype,
    quant_params: np.ndarray | None,
    dim: int,
    *,
    rotation_matrix: NDArray[np.float64] | None = None,
) -> NDArray[np.float32]:
    """Restore float32 vectors from their stored representation.

    Parameters
    ----------
    data : stored array (shape depends on dtype).
    dtype : the StoreDtype that was used to quantize.
    quant_params : per-vector quantization params (or ``None``).
    dim : original vector dimensionality (needed for sub-byte unpacking).
    rotation_matrix : optional
        Required for TurboQuant dtypes.
    """
    match dtype:
        case StoreDtype.FLOAT64 | StoreDtype.FLOAT32 | StoreDtype.FLOAT16:
            return data.astype(np.float32)
        case StoreDtype.BFLOAT16:
            return _bfloat16_to_float32(data)
        case StoreDtype.INT8_SYM:
            return _dequantize_int8_sym(data, quant_params)
        case StoreDtype.INT8_ASYM:
            return _dequantize_int8_asym(data, quant_params)
        case StoreDtype.FP4:
            return _dequantize_fp4(data, quant_params, dim)
        case StoreDtype.INT4:
            return _dequantize_int4(data, quant_params, dim)
        case StoreDtype.INT3:
            return _dequantize_int3(data, quant_params, dim)
        case StoreDtype.TURBO_2BIT:
            return _dequantize_turbo(data, 2, quant_params, dim, rotation_matrix)
        case StoreDtype.TURBO_3BIT:
            return _dequantize_turbo(data, 3, quant_params, dim, rotation_matrix)
        case StoreDtype.TURBO_4BIT:
            return _dequantize_turbo(data, 4, quant_params, dim, rotation_matrix)
        case _:  # pragma: no cover
            raise ValueError(f"Unsupported dtype: {dtype}")


# ---------------------------------------------------------------------------
# Sub-byte packing helpers
# ---------------------------------------------------------------------------


def pack_int4(values: np.ndarray) -> np.ndarray:
    """Pack an array of signed 4-bit values (range -8..7) into uint8.

    Two values per byte: low nibble first, then high nibble.
    *values* shape ``(n, dim)`` → output shape ``(n, ceil(dim/2))``.
    """
    n, dim = values.shape
    # Pad to even length if necessary
    if dim % 2:
        values = np.concatenate(
            [values, np.zeros((n, 1), dtype=values.dtype)], axis=1
        )
    # Mask to 4-bit unsigned representation
    masked = values.astype(np.uint8) & 0x0F
    low = masked[:, 0::2]
    high = masked[:, 1::2]
    return (low | (high << 4)).astype(np.uint8)


def unpack_int4(packed: np.ndarray, dim: int) -> np.ndarray:
    """Unpack uint8 array back to signed 4-bit values.

    Returns shape ``(n, dim)`` with dtype int8.
    """
    low = (packed & 0x0F).astype(np.int8)
    high = ((packed >> 4) & 0x0F).astype(np.int8)
    # Sign-extend from 4-bit
    low = (low ^ 0x08) - 0x08
    high = (high ^ 0x08) - 0x08
    # Interleave
    n = packed.shape[0]
    interleaved = np.empty((n, packed.shape[1] * 2), dtype=np.int8)
    interleaved[:, 0::2] = low
    interleaved[:, 1::2] = high
    return interleaved[:, :dim]


def pack_int3(values: np.ndarray) -> np.ndarray:
    """Pack signed 3-bit values (range -4..3) into uint8.

    8 values are packed into 3 bytes (24 bits).
    *values* shape ``(n, dim)`` → output shape ``(n, ceil(dim/8)*3)``.
    """
    n, dim = values.shape
    # Pad to multiple of 8
    pad = (8 - dim % 8) % 8
    if pad:
        values = np.concatenate(
            [values, np.zeros((n, pad), dtype=values.dtype)], axis=1
        )
    groups = values.shape[1] // 8  # number of 8-value groups
    # Mask to 3-bit unsigned representation
    masked = values.astype(np.uint32) & 0x07  # shape (n, groups*8)
    masked = masked.reshape(n, groups, 8)  # (n, groups, 8)

    # Pack 8 × 3-bit values into 3 bytes (24 bits)
    # Bit layout: byte0 = v0[2:0]|v1[2:0]|v2[1:0], byte1 = v2[2]|v3|v4|v5[1:0], byte2 = v5[2]|v6|v7|pad[1:0]
    # Simpler: treat as a 24-bit integer, little-endian.
    bits = np.zeros((n, groups), dtype=np.uint32)
    for i in range(8):
        bits |= masked[:, :, i] << (i * 3)

    packed = np.zeros((n, groups * 3), dtype=np.uint8)
    packed[:, 0::3] = (bits & 0xFF).astype(np.uint8)
    packed[:, 1::3] = ((bits >> 8) & 0xFF).astype(np.uint8)
    packed[:, 2::3] = ((bits >> 16) & 0xFF).astype(np.uint8)
    return packed


def unpack_int3(packed: np.ndarray, dim: int) -> np.ndarray:
    """Unpack uint8 array back to signed 3-bit values.

    Returns shape ``(n, dim)`` with dtype int8.
    """
    n = packed.shape[0]
    groups = packed.shape[1] // 3

    # Reconstruct 24-bit integers
    bits = (
        packed[:, 0::3].astype(np.uint32)
        | (packed[:, 1::3].astype(np.uint32) << 8)
        | (packed[:, 2::3].astype(np.uint32) << 16)
    )

    vals = np.zeros((n, groups, 8), dtype=np.int8)
    for i in range(8):
        raw = ((bits >> (i * 3)) & 0x07).astype(np.int8)
        # Sign-extend from 3-bit
        vals[:, :, i] = (raw ^ 0x04) - 0x04

    return vals.reshape(n, groups * 8)[:, :dim]


# ---------------------------------------------------------------------------
# BFloat16 helpers (numpy has no native bfloat16)
# ---------------------------------------------------------------------------


def _float32_to_bfloat16(arr: NDArray[np.float32]) -> NDArray[np.uint16]:
    """Truncate float32 to bfloat16 stored as uint16."""
    return arr.view(np.uint32)[:, :].astype(np.uint32).__rshift__(16).astype(np.uint16)


def _bfloat16_to_float32(arr: NDArray[np.uint16]) -> NDArray[np.float32]:
    """Expand bfloat16 (uint16) back to float32."""
    wide = arr.astype(np.uint32) << 16
    return wide.view(np.float32)


# ---------------------------------------------------------------------------
# INT8 symmetric
# ---------------------------------------------------------------------------


def _quantize_int8_sym(
    vectors: NDArray[np.float32],
) -> tuple[np.ndarray, np.ndarray]:
    abs_max = np.max(np.abs(vectors), axis=1, keepdims=True)  # (n, 1)
    # Avoid division by zero
    abs_max = np.where(abs_max == 0, 1.0, abs_max)
    scale = abs_max / 127.0  # (n, 1)
    quantized = np.clip(np.round(vectors / scale), -127, 127).astype(np.int8)
    return quantized, scale.astype(np.float32)


def _dequantize_int8_sym(
    data: np.ndarray, quant_params: np.ndarray | None
) -> NDArray[np.float32]:
    assert quant_params is not None
    scale = quant_params  # (n, 1)
    return data.astype(np.float32) * scale


# ---------------------------------------------------------------------------
# INT8 asymmetric
# ---------------------------------------------------------------------------


def _quantize_int8_asym(
    vectors: NDArray[np.float32],
) -> tuple[np.ndarray, np.ndarray]:
    vmin = np.min(vectors, axis=1, keepdims=True)  # (n, 1)
    vmax = np.max(vectors, axis=1, keepdims=True)  # (n, 1)
    span = vmax - vmin
    span = np.where(span == 0, 1.0, span)
    scale = span / 255.0  # (n, 1)
    zero_point = np.clip(np.round(-vmin / scale), 0, 255)  # (n, 1)
    quantized = np.clip(np.round(vectors / scale + zero_point), 0, 255).astype(
        np.uint8
    )
    # Store as int8 view for consistency (values 0..255 stored unsigned)
    params = np.concatenate([scale, zero_point], axis=1).astype(np.float32)  # (n, 2)
    return quantized.view(np.int8), params


def _dequantize_int8_asym(
    data: np.ndarray, quant_params: np.ndarray | None
) -> NDArray[np.float32]:
    assert quant_params is not None
    scale = quant_params[:, 0:1]  # (n, 1)
    zero_point = quant_params[:, 1:2]  # (n, 1)
    return (data.view(np.uint8).astype(np.float32) - zero_point) * scale


# ---------------------------------------------------------------------------
# FP4 (E2M1 mini-float with per-vector scale)
# ---------------------------------------------------------------------------

# E2M1 codebook: 1 sign bit, 2 exponent bits, 1 mantissa bit, bias=1.
# Positive values (codes 0-7): 0, 0.5, 1.0, 1.5, 2.0, 3.0, 4.0, 6.0
# Negative values (codes 8-15): -0, -0.5, -1.0, -1.5, -2.0, -3.0, -4.0, -6.0
_FP4_TABLE = np.array(
    [0.0, 0.5, 1.0, 1.5, 2.0, 3.0, 4.0, 6.0,
     -0.0, -0.5, -1.0, -1.5, -2.0, -3.0, -4.0, -6.0],
    dtype=np.float32,
)

# Sorted positive representable values and their midpoint boundaries
_FP4_POS_VALS = np.array([0.0, 0.5, 1.0, 1.5, 2.0, 3.0, 4.0, 6.0], dtype=np.float32)
_FP4_POS_BOUNDS = (_FP4_POS_VALS[:-1] + _FP4_POS_VALS[1:]) / 2  # midpoints


def _quantize_fp4(
    vectors: NDArray[np.float32],
) -> tuple[np.ndarray, np.ndarray]:
    abs_max = np.max(np.abs(vectors), axis=1, keepdims=True)
    abs_max = np.where(abs_max == 0, 1.0, abs_max)
    scale = abs_max / 6.0  # (n, 1) — 6.0 is the largest FP4 magnitude
    scaled = vectors / scale  # (n, dim)

    signs = (scaled < 0).astype(np.uint8)  # 0 or 1
    abs_scaled = np.abs(scaled)  # (n, dim)

    # Nearest FP4 code via searchsorted on positive boundaries
    codes = np.searchsorted(_FP4_POS_BOUNDS, abs_scaled).astype(np.uint8)  # 0..7
    codes = (signs << 3) | codes  # 4-bit FP4 code

    packed = pack_fp4(codes)
    return packed, scale.astype(np.float32)


def _dequantize_fp4(
    data: np.ndarray, quant_params: np.ndarray | None, dim: int
) -> NDArray[np.float32]:
    assert quant_params is not None
    scale = quant_params[:, 0:1]  # (n, 1)
    codes = unpack_fp4(data, dim)  # (n, dim) uint8 0..15
    return _FP4_TABLE[codes] * scale


def pack_fp4(codes: np.ndarray) -> np.ndarray:
    """Pack 4-bit FP4 codes (0..15) into uint8.

    Two values per byte: low nibble first.
    *codes* shape ``(n, dim)`` → ``(n, ceil(dim/2))``.
    """
    n, dim = codes.shape
    if dim % 2:
        codes = np.concatenate(
            [codes, np.zeros((n, 1), dtype=codes.dtype)], axis=1
        )
    low = codes[:, 0::2].astype(np.uint8)
    high = codes[:, 1::2].astype(np.uint8)
    return (low | (high << 4)).astype(np.uint8)


def unpack_fp4(packed: np.ndarray, dim: int) -> np.ndarray:
    """Unpack uint8 array back to 4-bit FP4 codes (0..15).

    Returns shape ``(n, dim)`` with dtype uint8.
    """
    low = packed & 0x0F
    high = (packed >> 4) & 0x0F
    n = packed.shape[0]
    interleaved = np.empty((n, packed.shape[1] * 2), dtype=np.uint8)
    interleaved[:, 0::2] = low
    interleaved[:, 1::2] = high
    return interleaved[:, :dim]


